
# insert methdo   //used to insert data at desired position

fruits1=["mango","orange"]
fruits1.insert(1,"grapes")
print(fruits1)

# for concatenate two list

fruits2=["grapes","apple"]
fruits=fruits1+fruits2
print(fruits)

# extend method      // to join two list

fruits1.extend(fruits2)
print(fruits1)

# append method       //to create list in list

fruits1.append(fruits2)
print(fruits1)